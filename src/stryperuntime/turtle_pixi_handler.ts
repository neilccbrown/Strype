    async setheading(rad: number, turtleID: number) {
        let turtle = this.turtles.get(turtleID)!;
        let startAngle = turtle.rotation;
        let endAngle = -rad;
        if (this.turtleMode == "logo") {
            startAngle = startAngle + Math.PI / 2;
            endAngle = rad;
        }
        startAngle = startAngle % (Math.PI * 2);
        endAngle = endAngle % (Math.PI * 2);
        if (endAngle < 0) {
            endAngle = Math.PI * 2 + endAngle;
        }
        if (startAngle < 0) {
            startAngle = Math.PI * 2 + startAngle;
        }
        if (Math.abs(endAngle - startAngle) < Math.PI) {
            await this.rotateTurtle(endAngle - startAngle, turtleID);
        }
        else {
            if (endAngle > startAngle) {
                await this.rotateTurtle(
                    -(Math.PI * 2) + endAngle - startAngle,
                    turtleID
                );
            }
            else {
                await this.rotateTurtle(Math.PI * 2 + endAngle - startAngle, turtleID);
            }
        }
    }


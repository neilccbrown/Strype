    end_fill(turtleID: number) {
        let turtle = this.turtles.get(turtleID)!;
        const tess = new TessGraphics();
        this.pixiApp.renderer.render(tess);
        const objID = this.findObjID(turtle);
        if (this.filling(turtleID)) {
            tess.lineStyle(1, turtle.fillcolor);
            tess.beginFill(turtle.fillcolor);
            tess.moveTo(
                Math.round(turtle.fillHistory[0].params[0] * 100) / 100,
                Math.round(turtle.fillHistory[0].params[1] * 100) / 100
            );
            turtle.fillHistory.forEach((e) => {
                if (e.name === "setPosition" || e.name === "moveTurtle") {
                    tess.lineTo(
                        Math.round(e.params[0] * 100) / 100,
                        Math.round(e.params[1] * 100) / 100
                    );
                }
                if (e.name === "arc") {
                    let clockWise = false;
                    if (e.params[5] < 0) {
                        clockWise = true;
                    }
                    tess.arc(
                        e.params[0],
                        e.params[1],
                        e.params[2],
                        e.params[3],
                        e.params[4],
                        clockWise
                    );
                }
            });
            tess.endFill();

            this.addObject(tess as PIXI.DisplayObject, objID, turtleID);
            turtle.fill = false;
            turtle.fillHistory = [];
        }
    }

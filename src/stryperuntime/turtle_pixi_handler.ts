    /**
   * prints a stamp on the canvas and returns a stamp id
   * @returns A number that represents the stamp id
   */
    stamp(turtleID: number) {
        let turtle = this.turtles.get(turtleID)!;
        return new Promise((resolve, reject) => {
            const graphics = new PIXI.Graphics();
            graphics.beginFill(turtle.fillcolor);
            graphics.lineStyle(1, turtle.pencolor);
            let points: number[] = [];
            if (turtle.shapeName === "circle") {
                graphics.drawCircle(0, 0, 10);
            }
            else {
                points = turtle.shapelist[turtle.shapeName];
                graphics.drawPolygon(points);
            }
            graphics.endFill();
            const stamp = graphics;
            let nextNumber = 5;
            while (turtle.stamps.includes(nextNumber)) {
                nextNumber++;
            }
            let objID = nextNumber;
            turtle.stamps.push(nextNumber);

            // size
            stamp.rotation = turtle.rotation;

            //position
            stamp.x = turtle.x;
            stamp.y = turtle.y;
            this.addObject(stamp as PIXI.DisplayObject, objID, turtleID);
            resolve(objID);
        });
    }
    /**clear a stamp by giving the stamp objID*/
    clearstamp(objID: number, turtleID: number) {
        let turtle = this.turtles.get(turtleID)!;
        this.removeObject(objID, turtleID);
        const index = turtle.stamps.indexOf(objID);
        turtle.stamps.splice(index, 1);
    }

    /**
   * depending on the parameter
   * with no parameter it deletes all the stamps
   * with a positive parameter it deletes the first stamps
   * with a negative paramter it deletes the last stamps
   * @param id is a number
   */
    clearstamps(objID: number, turtleID: number) {
        let turtle = this.turtles.get(turtleID)!;
        if (!objID) {
            for (let i = 0; i < turtle.stamps.length; i++) {
                this.removeObject(turtle.stamps[i], turtleID);
            }
            turtle.stamps.length = 0;
        }
        else if (objID > 0) {
            const firstEl = turtle.stamps.slice(0, objID);
            firstEl.forEach((el) => {
                this.removeObject(el, turtleID);
            });
            turtle.stamps.splice(0, objID);
        }
        else {
            const lastEl = turtle.stamps.slice(objID);
            lastEl.forEach((el) => {
                this.removeObject(el, turtleID);
            });
            turtle.stamps.splice(objID);
        }
    }

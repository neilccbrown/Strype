    shape(shape: string, turtleID: number) {
        let turtle = this.turtles.get(turtleID)!;
        let newShape : PIXI.DisplayObject;
        turtle.shapeName = shape;
        if (shape in turtle.svgList) {
            const texture = PIXI.Texture.from(turtle.svgList[shape]);
            const sprite = new PIXI.Sprite(texture);
            sprite.anchor.set(0.5);
            sprite.transform.scale.set(0.2);
            newShape = sprite as PIXI.DisplayObject;
        }
        else {
            const graphics = new PIXI.Graphics();
            graphics.beginFill(turtle.fillcolor);
            graphics.lineStyle(1, turtle.pencolor);
            let points: number[] = [];

            if (shape === "circle") {
                graphics.drawCircle(0, 0, 10);
            }
            else {
                points = turtle.shapelist[turtle.shapeName];
                graphics.drawPolygon(points);
            }
            graphics.endFill();
            newShape = graphics as PIXI.DisplayObject;
        }

        let parentContainer = turtle.shape.parent;
        let x = turtle.x;
        let y = turtle.y;
        let rotation = turtle.rotation;
        let isVisible = turtle.shape.visible;
        if (this.autoRepaint) {
            const index = parentContainer.getChildIndex(turtle.shape);
            parentContainer.removeChildAt(index);
            turtle.shape = newShape;
            parentContainer.addChildAt(turtle.shape, index);
        }
        else {
            turtle.shape = newShape;
        }
        turtle.rotation = rotation;
        turtle.x = x;
        turtle.y = y;
        turtle.shape.visible = isVisible;
    }


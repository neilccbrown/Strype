    async setposition(position: number[], turtleID: number) {
        let turtle = this.turtles.get(turtleID)!;
        let destX = position[0];
        let destY = position[1];
        if (this.filling(turtleID)) {
            turtle.fillHistory.push({
                name: "setPosition",
                params: [destX, destY],
            });
        }

        return new Promise((resolve, reject) => {
            let turtle = this.turtles.get(turtleID)!;
            if (turtle.shape === undefined) {
                reject(undefined);
            }
            else {
                const graphics = new PIXI.Graphics();
                const objID = this.findObjID(turtle);
                var startX = turtle.x;
                var startY = turtle.y;
                let direction = Math.atan2(destY - turtle.y, destX - turtle.x);

                let dx = Math.cos(direction);
                let dy = Math.sin(direction);

                let speed = this.setTickerSpeed(turtle.speed);

                const moveCallback = (delta: number) => {
                    graphics.clear();
                    graphics.lineStyle({
                        width: turtle.penWidth,
                        color: turtle.pencolor,
                        cap: this.lineCap[this.turtleMode],
                    });
                    graphics.moveTo(startX, startY);
                    if (
                        Math.abs(turtle.y - destY) > delta ||
            Math.abs(turtle.x - destX) > delta
                    ) {
                        turtle.x += dx * delta;
                        turtle.y += dy * delta;
                        if (turtle.penDown) {
                            graphics.lineTo(turtle.x, turtle.y);
                        }
                    }
                    else {
                        turtle.x = destX;
                        turtle.y = destY;
                        if (turtle.penDown) {
                            graphics.lineTo(turtle.x, turtle.y);
                        }
                        this.pixiApp.ticker.remove(moveCallback);
                        resolve(undefined);
                    }
                };
                graphics.lineStyle({
                    width: turtle.penWidth,
                    color: turtle.pencolor,
                    cap: this.lineCap[this.turtleMode],
                });
                graphics.moveTo(startX, startY);
                if (speed == 100) {
                    turtle.x = destX;
                    turtle.y = destY;
                    if (turtle.penDown) {
                        graphics.lineTo(destX, destY);
                        this.addObject(graphics as PIXI.DisplayObject, objID, turtleID);
                    }
                    resolve(undefined);
                }
                else {
                    this.addObject(graphics as PIXI.DisplayObject, objID, turtleID);
                    this.pixiApp.ticker.add(moveCallback);
                }
            }
        });
    }

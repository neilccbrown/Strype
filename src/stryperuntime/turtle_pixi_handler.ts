    /** move turtle sprite 'steps' number of pixels forwards (or backwards if steps is negative) */
    async moveTurtle(steps: number, turtleID: number) {
        let turtle = this.turtles.get(turtleID)!;

        if (turtle.shape === undefined) {
            return;
        }
        else {
            let dx = Math.cos(turtle.rotation);
            let dy = Math.sin(turtle.rotation);

            // if steps are negative, dx and dy point into the other direction
            if (steps < 0) {
                dx = -dx;
                dy = -dy;
                steps = -steps;
            }

            let destX = turtle.x + dx * steps;
            let destY = turtle.y + dy * steps;
            await this.setposition([destX, destY], turtleID);
        }
    }

    /**
   *
   * @param radius number of a the radius
   * @param angleRad number of the angle
   * @param left boolean if leftArc is called
   * @returns
   * Draws an arc with the given radius and angle relative to the turtle
   */
    async circle(radiusAngle: number[], turtleID: number) {
        let turtle = this.turtles.get(turtleID)!;
        let radius = -radiusAngle[0];
        let angleRad = radiusAngle[1];
        let steps = (radiusAngle[2] * Math.PI * 2) / angleRad;
        if (angleRad === 0 || steps <= 0) {
            return;
        }
        if (steps) {
            let segmentRad = (Math.PI * (steps - 2)) / steps;
            await this.rotateTurtle(-(Math.PI - segmentRad) / 2, turtleID);
            for (let i = 1; i < radiusAngle[2]; i++) {
                await this.moveTurtle(Math.cos(segmentRad / 2) * 2 * -radius, turtleID);
                await this.rotateTurtle(-Math.PI + segmentRad, turtleID);
            }
            await this.moveTurtle(Math.cos(segmentRad / 2) * 2 * -radius, turtleID);
            await this.rotateTurtle((-Math.PI + segmentRad) / 2, turtleID);
        }
        else {
            return new Promise((resolve, reject) => {
                const graphics = new PIXI.Graphics();
                if (turtle.shape === undefined) {
                    reject(undefined);
                }
                else {
                    if (!angleRad) {
                        angleRad = 2 * Math.PI;
                    }
                    // flips the direction from left to right if necessary.
                    let flipIfLeft = 1;
                    if (radius < 0) {
                        flipIfLeft = -1;
                    }
                    radius = Math.abs(radius);
                    // flips the direction from forward to backward if angle is negative.
                    let flipIfBackwards = 1;
                    if (angleRad < 0) {
                        flipIfBackwards = -1;
                    }
                    const objID = this.findObjID(turtle);
                    let startRotation = turtle.rotation;
                    let endRotation = startRotation + angleRad * flipIfLeft;
                    let startX = turtle.x;
                    let startY = turtle.y;
                    let centerX = startX - radius * Math.sin(startRotation) * flipIfLeft;
                    let centerY = startY + radius * Math.cos(startRotation) * flipIfLeft;
                    let DestX = centerX + Math.sin(endRotation) * radius * flipIfLeft;
                    let DestY = centerY - Math.cos(endRotation) * radius * flipIfLeft;

                    //fillHistory
                    if (this.filling(turtleID)) {
                        turtle.fillHistory.push({
                            name: "arc",
                            params: [
                                centerX,
                                centerY,
                                radius,
                                startRotation - (Math.PI / 2) * flipIfLeft,
                                endRotation - (Math.PI / 2) * flipIfLeft,
                                flipIfLeft * flipIfBackwards,
                            ],
                        });
                    }

                    let speed = this.setTickerSpeed(turtle.speed);
                    let deltaSum = 0;

                    //Animation of the turtle if speed is below 100
                    const moveCallback = (delta: number) => {
                        graphics.clear();
                        graphics.lineStyle({
                            width: turtle.penWidth,
                            color: turtle.pencolor,
                            cap: this.lineCap[this.turtleMode],
                        });
                        graphics.moveTo(startX, startY); // graphics.moveTo(startX,startY)
                        deltaSum += delta * flipIfBackwards;
                        let currentRotation =
              startRotation + (deltaSum / radius) * flipIfLeft;
                        if (Math.abs(currentRotation - endRotation) * radius > delta) {
                            const currentX =
                centerX + radius * Math.sin(currentRotation) * flipIfLeft;
                            const currentY =
                centerY - radius * Math.cos(currentRotation) * flipIfLeft;
                            turtle.x = currentX;
                            turtle.y = currentY;
                            if (turtle.penDown) {
                                graphics.arc(
                                    centerX,
                                    centerY,
                                    radius,
                                    startRotation - (Math.PI / 2) * flipIfLeft,
                                    currentRotation - (Math.PI / 2) * flipIfLeft,
                                    flipIfLeft * flipIfBackwards < 0
                                );
                            }
                            // Update turtle position
                            turtle.rotation = currentRotation;

                            // Schedule the next step
                        }
                        else {
                            // Stop the animation when the arc is fully drawn and sets the last step to the target coordinates
                            turtle.x = centerX + radius * Math.sin(endRotation) * flipIfLeft;
                            turtle.y = centerY - radius * Math.cos(endRotation) * flipIfLeft;
                            turtle.rotation = endRotation;
                            if (turtle.penDown) {
                                graphics.arc(
                                    centerX,
                                    centerY,
                                    radius,
                                    startRotation - (Math.PI / 2) * flipIfLeft,
                                    endRotation - (Math.PI / 2) * flipIfLeft,
                                    flipIfLeft * flipIfBackwards < 0
                                );
                            }
                            this.pixiApp.ticker.remove(moveCallback);
                            resolve(undefined);
                        }
                    };
                    graphics.lineStyle({
                        width: turtle.penWidth,
                        color: turtle.pencolor,
                        cap: this.lineCap[this.turtleMode],
                    });
                    if (speed == 100) {
                        turtle.x = DestX;
                        turtle.y = DestY;
                        turtle.rotation = endRotation;
                        if (turtle.penDown) {
                            graphics.clear();
                            graphics.lineStyle({
                                width: turtle.penWidth,
                                color: turtle.pencolor,
                                cap: this.lineCap[this.turtleMode],
                            });
                            graphics.arc(
                                centerX,
                                centerY,
                                radius,
                                startRotation - (Math.PI / 2) * flipIfLeft,
                                endRotation - (Math.PI / 2) * flipIfLeft,
                                flipIfLeft * flipIfBackwards < 0
                            );
                            this.addObject(graphics as PIXI.DisplayObject, objID, turtleID);
                        }
                        resolve(undefined);
                    }
                    else {
                        this.addObject(graphics as PIXI.DisplayObject, objID, turtleID);
                        this.pixiApp.ticker.add(moveCallback);
                    }
                }
            });
        }
    }


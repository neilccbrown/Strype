    /** rotates the turtle sprite 'deg' degrees to the right (or to the left if 'deg' is negative) */
    async rotateTurtle(rad: number, turtleID: number) {
        let turtle = this.turtles.get(turtleID)!;
        return new Promise((resolve, reject) => {
            var left = false;
            if (rad < 0) {
                left = true;
            }
            if (turtle.shape === undefined) {
                reject(undefined);
            }
            else {
                //move Sprite
                let speed = this.setTickerSpeed(turtle.speed);
                let rotationDest = turtle.rotation + rad;
                rad = Math.abs(rad);

                const turnCallback = (delta: number) => {
                    let radDelta = (delta / 180) * Math.PI;
                    if (Math.abs(rad) <= radDelta || speed == 100) {
                        turtle.rotation = rotationDest;
                        this.pixiApp.ticker.remove(turnCallback);
                        resolve(undefined);
                    }
                    else if (left) {
                        turtle.rotation -= radDelta;
                        rad -= radDelta;
                    }
                    else {
                        turtle.rotation += radDelta;
                        rad -= radDelta;
                    }
                };
                if (speed == 100) {
                    turtle.rotation = rotationDest;
                    resolve(undefined);
                }
                else {
                    this.pixiApp.ticker.add(turnCallback);
                }
            }
        });
    }

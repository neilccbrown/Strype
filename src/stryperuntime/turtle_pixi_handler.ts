    async write(
        text: [string, boolean, string, [string, number, string]],
        turtleID: number
    ) {
        let turtle = this.turtles.get(turtleID)!;
        const canvas = document.createElement("canvas");
        const context = canvas.getContext("2d");
    context!.font = text[3][2] + " " + text[3][1] + "px " + text[3][0];
    const metrics = context!.measureText(text[0]);
    let textWidth = turtle.x;
    let textHeight = -metrics.fontBoundingBoxAscent + turtle.y;
    if ("left" === text[2]) {
        textWidth = turtle.x;
    }
    else if ("center" === text[2]) {
        textWidth = turtle.x - metrics.width / 2;
    }
    else if ("right" === text[2]) {
        textWidth = turtle.x - metrics.width;
    }
    await this.writeHelper(text, [textWidth, textHeight], turtle);
    if (text[1]) {
        await this.setx(textWidth + metrics.width, turtleID);
    }
    }
    writeHelper(
        text: [string, boolean, string, [string, number, string]],
        endText: [number, number],
        turtle: TurtleState
    ) {
        return new Promise((resolve, reject) => {
            const objID = this.findObjID(turtle);
            const newText = new PIXI.Text(text[0], {
                fontFamily: text[3][0],
                fontSize: text[3][1],
                fontWeight: text[3][2] as PIXI.TextStyleFontWeight,
                fill: new PIXI.Color(turtle.pencolor) as any,
            });
            if (turtle.shape === undefined) {
                reject(undefined);
            }
            else {
                newText.x = endText[0];
                newText.y = endText[1];
            }
            this.addObject(newText as PIXI.DisplayObject, objID, turtle.id);
            resolve(undefined);
        });
    }

    getPixelColor(turtleID: number) {
        let turtle = this.turtles.get(turtleID)!;
        this.pixiApp.ticker.update();
        let container = this.autoRepaint
            ? (this.baseContainer as PIXI.DisplayObject)
            : (this.repaintContainer as PIXI.DisplayObject);
        const pixelColor = new PIXI.Color(
            this.pixiApp.renderer.extract.pixels(
                container,
                new PIXI.Rectangle(
                    turtle.x + this.panOffsetX - 1,
                    turtle.y + this.panOffsetY - 1,
                    1,
                    1
                )
            )
        ).toArray();
        if (pixelColor[3] < 255) {
            let bgcolor = new PIXI.Color(
                this.pixiApp.renderer.background.color
            ).toArray();
            const result = pixelColor.map((value: number, index: number) => {
                return value * pixelColor[3] + bgcolor[index] * (1 - pixelColor[3]);
            });
            result[3] = 255;
            return new PIXI.Color(result).toArray();
        }
        else {
            return new PIXI.Color(pixelColor).toArray();
        }
    }


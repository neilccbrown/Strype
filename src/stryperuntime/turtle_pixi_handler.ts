export async function handleTurtle(turtlePixiHandler : TurtlePixiHandler, prompts: unknown) {
    // arguments are parsed.
    // first element is the name of the function
    // second argument is the arguments used in the function
    // third argument is the id of the turtle if the function is called on a turtle
    let promptArray = prompts as [[string, unknown, number]];
    let result: any = null;

    for (let i = 0; i < promptArray.length; i++) {
        let prompt = promptArray[i];
        let funcName = prompt[0];
        let args = prompt[1];
        let turtleID = prompt[2];
        if (typeof turtleID !== "undefined") {
            if (typeof args !== "undefined") {
                // eslint-disable-next-line @typescript-eslint/ban-ts-comment
                // @ts-expect-error
                result = await turtlePixiHandler[funcName](args, turtleID);
            }
            else {
                // eslint-disable-next-line @typescript-eslint/ban-ts-comment
                // @ts-expect-error
                result = await turtlePixiHandler[funcName](turtleID);
            }
        }
        else {
            if (typeof args !== "undefined") {
                // eslint-disable-next-line @typescript-eslint/ban-ts-comment
                // @ts-expect-error
                result = await turtlePixiHandler[funcName](args);
            }
            else {
                // eslint-disable-next-line @typescript-eslint/ban-ts-comment
                // @ts-expect-error
                result = await turtlePixiHandler[funcName]();
            }
        }
    }
    return result;
}

